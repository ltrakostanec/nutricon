    private void btnSpremiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSpremiActionPerformed
        // TODO add your handling code here:
        //ako korisnik nije selektiran onda nedaj da se unos spremi u bazu 
        Mjerenje mjerenje = new Mjerenje();
        int index =tableOdabirKorisnika.getSelectedRow();
        if (index>-1) {
            DefaultTableModel tableModel = (DefaultTableModel)tableOdabirKorisnika.getModel();
            int id = (Integer)tableModel.getValueAt(index, 0);
            
            java.util.Date date;
            java.sql.Date sqldate;
            date = odabirDatuma.getDate();
            sqldate = new java.sql.Date(date.getTime());

            Double tezina = Double.parseDouble(txtTezina.getText());
            Double visina = Double.parseDouble(txtVisina.getText());

            Double opsegStruka = Double.parseDouble(txtOpsegStruka.getText());
            Double opsegBokova = Double.parseDouble(txtOpsegBokova.getText());
            
            String dnevnaAktivnost = (String) cmbAktivnost.getSelectedItem();
                
            mjerenje.setDnevnaAktivnost(dnevnaAktivnost);
            mjerenje.setDatumMjerenja(sqldate);
            mjerenje.setTezina(tezina);
            mjerenje.setVisina(visina);
            mjerenje.setOpsegStruka(opsegStruka);
            mjerenje.setOpsegBokova(opsegBokova);

            DBspajanje.unosMjerenja(mjerenje,id);   
            
            JOptionPane.showMessageDialog(null, "Mjerenje je uneseno",
                        "Unos Mjerenja", JOptionPane.PLAIN_MESSAGE);
            
            txtTezina.setText("");
            txtVisina.setText("");
            txtOpsegStruka.setText("");
            txtOpsegBokova.setText("");

        }
        else {
                JOptionPane.showMessageDialog(null, "Greška kod unosa. "
                        + "Mjerenje nije uneseno!\nKorisnik mora biti selektiran!", "Unos mjerenja",
                    JOptionPane.ERROR_MESSAGE);                
            }
                   
    }//GEN-LAST:event_btnSpremiActionPerformed

