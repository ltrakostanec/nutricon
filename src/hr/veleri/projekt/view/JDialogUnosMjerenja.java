    private void prikaziKorisnike() {
        DefaultTableModel tableModel = (DefaultTableModel)tableOdabirKorisnika.getModel();
        tableModel.setRowCount(0);
        try {
        ArrayList<Korisnik> listaKorisnika = DBspajanje.ucitajKorisnike();
        for (Korisnik korisnik: listaKorisnika)
            tableModel.addRow(new Object[]{korisnik.getId(),korisnik.getIme(),korisnik.getPrezime(),korisnik.getOib(),
                korisnik.getRodjendan(),korisnik.getSpol()});

        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Nema konekcije na bazu. Kontaktirajte administratora."
               ,"Greška kod dohvata korisnika"+ex.toString(), JOptionPane.ERROR_MESSAGE);
        }
    }
